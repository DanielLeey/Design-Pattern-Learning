package com.lee.constructor.origin;

/**
 * 产品类
 */
public class Bike {
    //自行车 车架
    private String frame;

    //自行车 车座
    private String seat;

    public String getFrame() {
        return frame;
    }

    public void setFrame(String frame) {
        this.frame = frame;
    }

    public String getSeat() {
        return seat;
    }

    public void setSeat(String seat) {
        this.seat = seat;
    }
}
